package common;

public class Utilities {

	public static int convertLetterToInt(String val)
    {
        int toReturn = -1;
        switch (val)
        {
            case "A":   toReturn = 1;
                        break;
            case "B":   toReturn = 2;
                        break;
            case "C":   toReturn = 3;
                        break;
            case "D":   toReturn = 4;
                        break;
            case "E":   toReturn = 5;
                        break;
            case "F":   toReturn = 6;
                        break;
            case "G":   toReturn = 7;
                        break;
            case "H":   toReturn = 8;
                        break;
            case "I":   toReturn = 9;
                        break;
            case "J":   toReturn = 10;
                        break;
            case "K":   toReturn = 11;
                        break;
            case "L":   toReturn = 12;
                        break;
            case "M":   toReturn = 13;
                        break;
            case "N":   toReturn = 14;
                        break;
            case "O":   toReturn = 15;
                        break;
            case "P":   toReturn = 16;
                        break;
            case "Q":   toReturn = 17;
                        break;
            case "R":   toReturn = 18;
                        break;
            case "S":   toReturn = 19;
                        break;
            case "T":   toReturn = 20;
                        break;
            case "U":   toReturn = 21;
                        break;
            case "V":   toReturn = 22;
                        break;
            case "W":   toReturn = 23;
                        break;
            case "X":   toReturn = 24;
                        break;
            case "Y":   toReturn = 25;
                        break;
            case "Z":   toReturn = 26;
                        break;
           
            default:    toReturn = -1;
                        break;
        }
        
        return toReturn;
    }
}
